package pl.pawel.linkshell.speechy.model;

public class PunctuationMark {
    private int period;
    private int question;
    private int exclamation;

    private int comma;
    private int semicolon;
    private int colon;
    private int dash;
    private int openBracket;
    private int closeBracket;
    private int openBrace;
    private int closeBrace;
    private int openParentheses;
    private int closeParentheses;
    private int apostrophe;
    private int quotation;
    private int ellipsis;

    public void setPeriod(final int period) {
        this.period = period;
    }

    public int getPeriod() {
        return period;
    }

    public void setQuestion(final int question) {
        this.question = question;
    }

    public int getQuestion() {
        return question;
    }

    public void setExclamation(final int exclamation) {
        this.exclamation = exclamation;
    }

    public int getExclamation() {
        return exclamation;
    }

    public void setComma(final int comma) {
        this.comma = comma;
    }

    public int getComma() {
        return comma;
    }

    public void setSemicolon(final int semicolon) {
        this.semicolon = semicolon;
    }

    public int getSemicolon() {
        return semicolon;
    }

    public void setColon(final int colon) {
        this.colon = colon;
    }

    public int getColon() {
        return colon;
    }

    public void setDash(final int dash) {
        this.dash = dash;
    }

    public int getDash() {
        return dash;
    }

    public void setOpenBrackets(final int openBracket) {
        this.openBracket = openBracket;
    }

    public int getOpenBracket() {
        return openBracket;
    }

    public void setCloseBrackets(final int closeBracket) {
        this.closeBracket = closeBracket;
    }

    public int getCloseBracket() {
        return closeBracket;
    }

    public void setOpenBraces(final int openBrace) {
        this.openBrace = openBrace;
    }

    public int getOpenBrace() {
        return openBrace;
    }

    public void setCloseBraces(final int closeBrace) {
        this.closeBrace = closeBrace;
    }

    public int getCloseBrace() {
        return closeBrace;
    }

    public void setOpenParentheses(final int openParentheses) {
        this.openParentheses = openParentheses;
    }

    public int getOpenParentheses() {
        return openParentheses;
    }

    public void setCloseParentheses(final int closeParentheses) {
        this.closeParentheses = closeParentheses;
    }

    public int getCloseParentheses() {
        return closeParentheses;
    }

    public void setApostrophe(final int apostrophe) {
        this.apostrophe = apostrophe;
    }

    public int getApostrophe() {
        return apostrophe;
    }

    public void setQuotation(final int quotation) {
        this.quotation = quotation;
    }

    public int getQuotation() {
        return quotation;
    }

    public void setEllipsis(final int ellipsis) {
        this.ellipsis = ellipsis;
    }

    public int getEllipsis() {
        return ellipsis;
    }
}
